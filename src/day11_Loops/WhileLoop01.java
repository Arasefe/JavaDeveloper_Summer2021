package day11_Loops;

public class WhileLoop01 {

	public static void main(String[] args) {
		
		/*
		 	Print "Hello!" 5 times on the console
		*/
		
		for(int i=1; i<6; i++) {
			System.out.println("Hello!");
		}
		
		System.out.println("=====================");
		
		int i=1;
		
		while(i<4) {			
			System.out.println("Hello!");			
			i++;			
		}

	}

}
